package Ex_day16June_Switch_Case;

public class Lab096_Interview {
    public static void main(String[] args) {
        char code = 'C';

        switch (code) {
            default:
                System.out.println("Helloooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("65");
                break;
        }
    }
}
