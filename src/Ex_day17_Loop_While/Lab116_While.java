package Ex_day17_Loop_While;

public class Lab116_While {
    public static void main(String[] args) {
        int i = 0;
        while (i<10){
            System.out.println(i);
            i++;
        }
        int j =0;
        for (; j<10; j++){
            System.out.println(j);

        }
    }
}
