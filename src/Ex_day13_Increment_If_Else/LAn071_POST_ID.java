package Ex_day13_Increment_If_Else;

public class LAn071_POST_ID {
    public static void main(String[] args) {
        int a =10 ;
        System.out.println(++a);
        System.out.println(a);

        // post increment  = print first then increase
        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
