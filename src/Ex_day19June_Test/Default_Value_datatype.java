package Ex_day19June_Test;

public class Default_Value_datatype {

    /* Write a Java program to declare variables of all
    primitive data types and print their default values. */

    static int a;
    static short s;
    static long l;
    static double d;
   static boolean b;
   static byte B;
    static char ch;
    static float f;
    public static void main(String[] args) {

        System.out.println("Default value of int " + a);
        System.out.println("Default value of short" + s);
        System.out.println("Default value of long" + l);
        System.out.println("Default value of double" + d);
        System.out.println("Default value of boolean" + b);
        System.out.println("Default value of byte" + B);
        System.out.println("Default value of float" + f);
        System.out.println("Default value of char" + ch);

    }
}
